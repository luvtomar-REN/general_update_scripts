UPDATE OrderTicket SET WorkOrderNo = REPLACE(REPLACE(WorkOrderNo,'WAR',''),'RMA',''), WareHouse = 'Telus' WHERE WorkOrderId = 402869;
UPDATE T009_CUSTOMER_ORDERS SET C009_NOTES = REPLACE(CAST(C009_NOTES AS NVARCHAR),'WARRANTY','REPAIR'), C009_TOTALPRICE = 525.0000 WHERE C009_ORDER_ID = (SELECT SalesOrderID FROM OrderTicket WHERE WorkOrderId = 402869);
UPDATE T009_WORK_ORDERS SET C009_NOTES = REPLACE(CAST(C009_NOTES AS NVARCHAR),'WARRANTY','REPAIR'), C009_WAREHOUSE_ID = 65, C009_PICK_LOC_ID = 2145, C009_WORK_ORDER_NO = REPLACE(REPLACE(C009_WORK_ORDER_NO,'WAR',''),'RMA',''), C009_CUSTOM_FIELD1 = '001', C009_TOTAL_COST = 525.0000
WHERE C009_ORDER_ID = 402869;
UPDATE T010_CUSTOMER_ORDER_DETAILS SET C010_PRODUCT_NOTES = 'REPAIR', C010_PRICE = 525.0000, C010_PRICE_INVOICE = 525.0000 WHERE C010_ORDER_ID = (SELECT SalesOrderID FROM OrderTicket WHERE WorkOrderId = 402869);
UPDATE T010_WORK_ORDER_DETAILS SET C010_COST = 525.0000 WHERE C010_ORDER_ID = 402869;
UPDATE Common_PreAlert SET WorkOrderNum = REPLACE(REPLACE(WorkOrderNum,'WAR',''),'RMA','') WHERE WorkOrderNum LIKE '%-402869';
UPDATE SerialNum SET WHID = 65, PicLocation = 'Receiving', PICK_LOC_ID = 2145 WHERE (WorkOrderId = 402869 OR OnWorkOrderId = 402869);
UPDATE RenaissanceDW.dbo.DailyCustomerOrderMetrics SET price_per_quantity = 525.0000, total_price = 525.0000 WHERE work_order_id = 402869;






